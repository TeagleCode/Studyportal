const db = require('../db');
const J = JSON.stringify;

// opts[0] = correct formula, rest are wrong
function pmc(text, vars, formula, opts, expl) {
  return {
    question_text: text, question_type: 'multiple_choice', is_parametric: 1,
    variables: J(vars), answer_formula: formula,
    option_formulas: J(opts.map((f, i) => ({ formula: f, is_correct: i === 0 }))),
    explanation_template: expl || null, explanation: null,
  };
}
// answers = [['text', isCorrect], ...]
function smc(text, answers, expl) {
  return { question_text: text, question_type: 'multiple_choice', is_parametric: 0, explanation: expl || null, answers };
}

async function addQs(conn, tid, qs) {
  for (const q of qs) {
    const [r] = await conn.execute(
      `INSERT INTO questions (topic_id, question_text, question_type, is_parametric, variables,
       answer_formula, option_formulas, explanation_template, explanation)
       VALUES (?,?,?,?,?,?,?,?,?)`,
      [tid, q.question_text, q.question_type, q.is_parametric,
       q.variables||null, q.answer_formula||null, q.option_formulas||null,
       q.explanation_template||null, q.explanation||null]
    );
    if (q.answers) {
      for (const [t, c] of q.answers)
        await conn.execute('INSERT INTO answers (question_id, answer_text, is_correct) VALUES (?,?,?)', [r.insertId, t, c?1:0]);
    }
  }
}

